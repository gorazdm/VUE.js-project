function available(allGames) {
    for (let game of allGames) {
        if (Math.floor(Math.random() * 2) === 1) {
        game.forTrade = true;
        } else {
        game.forTrade = false;
        }
    }
}
function setPrice(allGames) {
    for (let game of allGames) {
        let price = Math.floor(Math.random() * 100);
        game.price = price;
    }
}
